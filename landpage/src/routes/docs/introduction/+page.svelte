			<div class="rounded-lg border border-zinc-800 p-4" style="background:#0C0C0E">
				<h3 class="text-sm font-semibold text-emerald-400">No string to leak</h3>
				<p class="mt-1 text-sm text-zinc-400">The private key is generated on the device and never exported. You can commit your entire codebase to a public repo and nothing is compromised.</p>
			</div>
			<div class="rounded-lg border border-zinc-800 p-4" style="background:#0C0C0E">
				<h3 class="text-sm font-semibold text-emerald-400">No rotation</h3>
				<p class="mt-1 text-sm text-zinc-400">Device keys don't expire. If a device is compromised, revoke it instantly with <code class="font-mono text-xs">amesh revoke</code> — only that device loses access.</p>
			</div>
			<div class="rounded-lg border border-zinc-800 p-4" style="background:#0C0C0E">
				<h3 class="text-sm font-semibold text-emerald-400">Per-machine identity</h3>
				<p class="mt-1 text-sm text-zinc-400">Every request arrives with a verified device ID. You know exactly which machine sent it, when, and with what payload. Per-device audit trail comes for free.</p>
			</div>
			<div class="rounded-lg border border-zinc-800 p-4" style="background:#0C0C0E">
				<h3 class="text-sm font-semibold text-emerald-400">Cryptographic proof of origin</h3>
				<p class="mt-1 text-sm text-zinc-400">Each request is signed with a key bound to the device. The signature is non-repudiable — the device can't deny it sent the request, and no other device could have produced the same signature.</p>
			</div>
