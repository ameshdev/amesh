			<li class="flex gap-2"><span class="text-emerald-400">▸</span> <span><strong class="text-zinc-300">Private keys never leave the device.</strong> The KeyStore interface takes a message and returns a signature — the key itself is never exposed to application code.</span></li>
			<li class="flex gap-2"><span class="text-emerald-400">▸</span> <span><strong class="text-zinc-300">Hardware-first, graceful fallback.</strong> Secure Enclave on Apple Silicon, TPM 2.0 on Linux, macOS Keychain as a second tier, encrypted file (Argon2id) only on cloud VMs where hardware isn't available.</span></li>
			<li class="flex gap-2"><span class="text-emerald-400">▸</span> <span><strong class="text-zinc-300">One-way trust.</strong> Controllers authenticate TO targets, never the reverse. A compromised server cannot call back to your laptop.</span></li>
			<li class="flex gap-2"><span class="text-emerald-400">▸</span> <span><strong class="text-zinc-300">Open cryptography only.</strong> Built on audited <code class="font-mono text-xs">@noble/curves</code>, <code class="font-mono text-xs">@noble/hashes</code>, and <code class="font-mono text-xs">@noble/ciphers</code>. P-256 ECDSA, SHA-256 HMAC, ChaCha20-Poly1305.</span></li>
			<li class="flex gap-2"><span class="text-emerald-400">▸</span> <span><strong class="text-zinc-300">Stateless relay.</strong> The pairing relay holds no persistent state — it just brokers a WebSocket handshake. You can shut it down after pairing.</span></li>
