		<p class="mt-3 text-zinc-400">
			Almost every backend authenticates other services using static strings stored in <code class="rounded bg-emerald-400/10 px-1.5 py-0.5 text-sm text-emerald-400">.env</code> files,
			environment variables, or secrets managers. The model has a fundamental flaw: <strong class="text-zinc-300">the secret IS the identity</strong>.
			Anyone who has the string is authenticated. There is no way to prove <em>which machine</em> used it, whether it was copied, or where it's been.
		</p>
		<div class="mt-6 space-y-3">
			<div class="rounded-lg border-l-2 border-red-400/40 bg-red-950/20 px-4 py-3">
				<p class="text-sm font-medium text-red-300">GitHub secret scanning detected over 1 million leaked secrets in public repos in 2024 alone.</p>
				<p class="mt-1 text-xs text-zinc-500">Every year, major breaches trace back to leaked credentials. The leak vectors are mundane: .env files committed to git, secrets printed to logs, Docker images pushed to public registries, CI/CD build logs.</p>
			</div>
		</div>
		<p class="mt-4 text-zinc-400">
			Rotation is painful, there's no per-machine identity, and secrets managers only move the problem — the secret still
			exists as a copyable string, still needs its own authentication, and still adds latency on every cold start.
		</p>
	</section>

